public class Operaciones {
    /**
     * Metodo que devuelve la suma de dos numeros
     * @param num1
     * @param num2
     * @return la suma de los dos numeros
     * 
     */
    
    public static float suma(float num1, float num2) {
        return num1 + num2;
        }
    
    
      /**
      * Metodo que multiplica dos numeros pasados por parametros
      * @param num1
      * @param num2
      * @return la multiplicacion de ambos numeros
      */
     public static float multiplicacion(float num1, float num2) {
        return num1 * num2;
    }
     
}
