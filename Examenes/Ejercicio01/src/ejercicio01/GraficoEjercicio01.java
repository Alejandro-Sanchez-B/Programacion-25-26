    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
     
       try {
            
            double altura = Double.parseDouble(jTextField1.getText());
            int peso = jSlider1.getValue();

            if (altura <= 0) {
                jLabel4.setText("La altura debe ser mayor que 0.");
                return;
            }

            
            double imc = peso / (altura*2);

            jLabel4.setText(String.format("IMC: %.2f", imc));

        } catch (NumberFormatException ex) {
            jLabel4.setText("Introduce una altura vÃ¡lida.");
        } 
        
    }//GEN-LAST:event_jButton1ActionPerformed

